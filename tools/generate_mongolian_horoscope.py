def main():
    random.seed(42)
    dates = []
    # Feb 28 (for getBundle on Feb 29)
    dates.append((2026, 2, 28))
    # March 1-31
    for d in range(1, 32):
        dates.append((2026, 3, d))
    # April 1-30
    for d in range(1, 31):
        dates.append((2026, 4, d))
    # May 1-2 (May 2 for getBundle on May 1)
    dates.append((2026, 5, 1))
    dates.append((2026, 5, 2))
    
    signs = ['Aries', 'Taurus', 'Gemini', 'Cancer', 'Leo', 'Virgo', 
             'Libra', 'Scorpio', 'Sagittarius', 'Capricorn', 'Aquarius', 'Pisces']
    
    output = []
    for idx, (y, m, d) in enumerate(dates):
        date_key = f"'{y}-{m:02d}-{d:02d}'"
        day_num = idx  # unique per date
        
        paras = []
        for sign_idx, sign in enumerate(signs):
            p = gen_paragraph(sign, day_num, sign_idx)
            paras.append(f"      '{escape_dart(p)}'")
        
        output.append(f"    {date_key}: _mongolianDay(\n" + ",\n".join(paras) + ",\n    ),")
    
    with open("lib/data/horoscope_mongolian_generated.dart", "w", encoding="utf-8") as f:
        f.write("// Generated Mongolian horoscope content - merge into horoscope_content_prewritten.dart\n\n")
        f.write("\n".join(output))

if __name__ == "__main__":
    main()
